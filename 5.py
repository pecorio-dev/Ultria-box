_SUSPICIOUS_DIRS = [
    os.environ.get("TEMP", ""),
    os.environ.get("TMP", ""),
    os.environ.get("APPDATA", ""),
    os.path.join(os.environ.get("LOCALAPPDATA", ""), "Temp"),
    os.path.join(os.environ.get("USERPROFILE", ""), "AppData", "Roaming"),
    r"C:\Users\Public",
    r"C:\Windows\Temp",
]
_SUSPICIOUS_DIRS = [d.lower() for d in _SUSPICIOUS_DIRS if d]

_VOWELS = set("aeiouAEIOU")
_SYSTEM_NAMES = {
    "svchost.exe","lsass.exe","csrss.exe","wininit.exe","winlogon.exe",
    "services.exe","smss.exe","explorer.exe","taskhost.exe","taskhostw.exe",
    "dwm.exe","conhost.exe","spoolsv.exe","SearchIndexer.exe",
    "python.exe","pythonw.exe","cmd.exe","powershell.exe",
}

_MALWARE_KW = [
    "webhook","discord","telegram","keylog","stealer","rat","trojan",
    "payload","inject","grab","spy","hook","dump","token",
]

def _is_suspicious_name(name: str) -> str | None:
    base = os.path.splitext(name)[0].lower()
    if len(base) < 4:
        return "very short name"
    vowel_ratio = sum(1 for c in base if c in _VOWELS) / max(len(base), 1)
    if vowel_ratio < 0.10 and len(base) >= 5:
        return "no vowels (random-looking)"
    typos = {
        "svch0st","svchost32","svchost64","lsasss","csrss32",
        "explore","exploler","wscript32","wscript64","rundl132",
        "taskmgr32","regedt32","noteapd","chrom","googl",
    }
    if base in typos:
        return "typosquatting system name"
    if re.match(r'^[a-z]{1,3}[0-9]+[a-z]{0,3}$', base):
        return "alphanumeric random pattern"
    return None

def _is_suspicious_path(path: str) -> str | None:
    pl = path.lower()
    for d in _SUSPICIOUS_DIRS:
        if d and pl.startswith(d):
            return f"in suspicious dir: {os.path.dirname(path)}"
    if pl.endswith(".pyw") or pl.endswith(".py"):
        return "Python script as startup"
    for kw in _MALWARE_KW:
        if kw in pl:
            return f"keyword '{kw}' in path"
    return None

def _score(name: str, path: str) -> list[str]:
    reasons = []
    r = _is_suspicious_name(os.path.basename(name))
    if r: reasons.append(r)
    r = _is_suspicious_path(path)
    if r: reasons.append(r)
    return reasons

# ══════════════════════════════════════════════════════════════════════════════
#  BINARY SIGNATURE SCANNER  — detects builds generated by this builder
# ══════════════════════════════════════════════════════════════════════════════

_BUILDER_SIGS = [
    b'v4r_c2_wh',
    b'v4r_c2sub',
    b'_v4r_post',
    b'ClaudeCodeUpdater',
    b'WindowsUpdateService',
    b'discord.com/api/webhooks',
    b'discord.com\\api\\webhooks',
    b'__WEBHOOK__',
]
_MAX_BIN_SCAN = 4 * 1024 * 1024  # 4 MB cap

def _scan_binary(path: str) -> str | None:
    """Return reason string if the binary matches builder signatures, else None."""
    try:
        size = os.path.getsize(path)
        if size > 80 * 1024 * 1024:
            return None
        with open(path, 'rb') as f:
            data = f.read(min(size, _MAX_BIN_SCAN))
        for sig in _BUILDER_SIGS:
            if sig in data:
                return f'builder signature: {sig.decode("utf-8", "ignore")}'
    except Exception:
        pass
    return None

