_full_top, _full_scroll, _full_bot = _tab_skeleton(tabs.tab("Full Scan"))
_full_reg_sus:  list[dict] = []
_full_proc_sus: list[dict] = []
_full_file_sus: list[dict] = []

def _full_reg_del(item):
    if messagebox.askyesno("Confirm", f"Delete registry entry '{item['name']}'?"):
        _delete_registry_value(item["hive"], item["key"], item["name"])
        _set_status(f"Deleted '{item['name']}'")
        _do_full_scan()

def _full_proc_kill(item):
    if messagebox.askyesno("Confirm", f"Kill '{item['name']}' PID {item['pid']}?"):
        kill_process(item["pid"], item["name"])
        _set_status(f"Killed '{item['name']}'")
        _do_full_scan()

def _full_file_del(item):
    if messagebox.askyesno("Confirm", f"Delete '{item['path']}'?"):
        delete_file(item["path"])
        _set_status(f"Deleted '{item['name']}'")
        _do_full_scan()

def _full_clean_all():
    total = len(_full_reg_sus) + len(_full_proc_sus) + len(_full_file_sus)
    if total == 0: return
    if not messagebox.askyesno("Confirm Clean All", f"Remove ALL {total} threats?\nThis is permanent."):
        return
    for e in _full_reg_sus:
        _delete_registry_value(e["hive"], e["key"], e["name"])
    for p in _full_proc_sus:
        kill_process(p["pid"], p["name"])
    for f in _full_file_sus:
        delete_file(f["path"])
    _set_status(f"Cleaned {total} threats.")
    _do_full_scan()

def _do_full_scan():
    global _full_reg_sus, _full_proc_sus, _full_file_sus
    _set_status("Running full scan…")
    _btn_full_scan.configure(state="disabled")
    _clear_frame(_full_scroll)

    def _worker():
        global _full_reg_sus, _full_proc_sus, _full_file_sus
        try:    reg   = scan_registry()
        except: reg   = []
        try:    procs = scan_processes()
        except: procs = []
        try:    files = scan_files()
        except: files = []
        _full_reg_sus  = [e for e in reg   if e["reasons"]]
        _full_proc_sus = [p for p in procs if p["reasons"]]
        _full_file_sus = list(files)
        root.after(0, lambda: _full_render(reg, procs, files))

    def _full_render(reg, procs, files):
        sus_r = [e for e in reg   if e["reasons"]]
        sus_p = [p for p in procs if p["reasons"]]
        total = len(sus_r) + len(sus_p) + len(files)

        # Summary card
        card = ctk.CTkFrame(_full_scroll, fg_color=colors["dark_gray"], corner_radius=6)
        card.pack(fill="x", padx=4, pady=(4, 10))
        c_r = colors["red"] if sus_r else "#2d7a4f"
        c_p = colors["red"] if sus_p else "#2d7a4f"
        c_f = colors["red"] if files  else "#2d7a4f"
        ctk.CTkLabel(card, text=f"Registry   : {len(sus_r)} suspicious / {len(reg)} entries",
                     text_color=c_r, font=ctk.CTkFont(size=12), anchor="w").pack(fill="x", padx=14, pady=(8,2))
        ctk.CTkLabel(card, text=f"Processes  : {len(sus_p)} suspicious / {len(procs)} total",
                     text_color=c_p, font=ctk.CTkFont(size=12), anchor="w").pack(fill="x", padx=14, pady=2)
        ctk.CTkLabel(card, text=f"Files      : {len(files)} found in drop zones",
                     text_color=c_f, font=ctk.CTkFont(size=12), anchor="w").pack(fill="x", padx=14, pady=(2,8))

        idx = 0
        if sus_r:
            ctk.CTkLabel(_full_scroll, text="  Registry — Startup Keys",
                         font=ctk.CTkFont(size=11, weight="bold"),
                         text_color=colors["red"]).pack(anchor="w", padx=8, pady=(2,0))
            for item in sus_r:
                _make_row(_full_scroll, item, idx, action_cb=_full_reg_del, action_label="Delete")
                idx += 1

        if sus_p:
            ctk.CTkLabel(_full_scroll, text="  Processes — Running",
                         font=ctk.CTkFont(size=11, weight="bold"),
                         text_color=colors["red"]).pack(anchor="w", padx=8, pady=(6,0))
            for item in sus_p:
                _make_row(_full_scroll, item, idx, action_cb=_full_proc_kill, action_label="Kill")
                idx += 1

        if files:
            ctk.CTkLabel(_full_scroll, text="  Files — Drop Zones",
                         font=ctk.CTkFont(size=11, weight="bold"),
                         text_color=colors["red"]).pack(anchor="w", padx=8, pady=(6,0))
            for item in files:
                _make_row(_full_scroll, item, idx, action_cb=_full_file_del, action_label="Delete")
                idx += 1

        if total == 0:
            ctk.CTkLabel(_full_scroll, text="✓  System looks clean.",
                         text_color="#2d7a4f",
                         font=ctk.CTkFont(size=14, weight="bold")).pack(pady=36)

        _set_status(f"Full scan complete  ·  {total} threat(s) found")
        _btn_full_scan.configure(state="normal")
        _btn_full_clean.configure(state="normal" if total > 0 else "disabled")

    threading.Thread(target=_worker, daemon=True).start()

ctk.CTkLabel(_full_top, text="Full scan  ·  Registry + Processes + Drop Zones",
             font=ctk.CTkFont(size=12), text_color=colors["light_gray"]).pack(side="left", padx=10)
_btn_full_scan = ctk.CTkButton(_full_top, text="Run Full Scan", width=130, height=32,
                                fg_color=colors["red"], hover_color=colors["dark_red"],
                                font=ctk.CTkFont(size=12, weight="bold"),
                                command=_do_full_scan)
_btn_full_scan.pack(side="right", padx=10)

