#  REGISTRY SCANNER
# ══════════════════════════════════════════════════════════════════════════════

_RUN_KEYS = [
    (winreg.HKEY_CURRENT_USER,  r"Software\Microsoft\Windows\CurrentVersion\Run"),
    (winreg.HKEY_CURRENT_USER,  r"Software\Microsoft\Windows\CurrentVersion\RunOnce"),
    (winreg.HKEY_LOCAL_MACHINE, r"Software\Microsoft\Windows\CurrentVersion\Run"),
    (winreg.HKEY_LOCAL_MACHINE, r"Software\Microsoft\Windows\CurrentVersion\RunOnce"),
    (winreg.HKEY_LOCAL_MACHINE, r"Software\WOW6432Node\Microsoft\Windows\CurrentVersion\Run"),
    (winreg.HKEY_CURRENT_USER,  r"Software\Microsoft\Windows NT\CurrentVersion\Windows"),
]

_HIVE_NAMES = {
    winreg.HKEY_CURRENT_USER:  "HKCU",
    winreg.HKEY_LOCAL_MACHINE: "HKLM",
}

def scan_registry() -> list[dict]:
    results = []
    for hive, key_path in _RUN_KEYS:
        try:
            hk = winreg.OpenKey(hive, key_path, 0, winreg.KEY_READ)
            i = 0
            while True:
                try:
                    name, data, _ = winreg.EnumValue(hk, i)
                    if not isinstance(data, str):
                        data = str(data)
                    path = data.strip().strip('"').split('"')[0].split(' ')[0] if '"' not in data else data.split('"')[1]
                    reasons = _score(name, path)
                    # Deep-scan the registered executable for builder signatures
                    if path and os.path.isfile(path) and path.lower().endswith('.exe'):
                        sig = _scan_binary(path)
                        if sig and sig not in reasons:
                            reasons.append(sig)
                    results.append({
                        "hive":    _HIVE_NAMES.get(hive, "?"),
                        "key":     key_path,
                        "name":    name,
                        "path":    path,
                        "raw":     data,
                        "reasons": reasons,
                    })
                    i += 1
                except OSError:
                    break
            winreg.CloseKey(hk)
        except (OSError, PermissionError):
            pass
    return results

def _delete_registry_value(hive_str: str, key_path: str, name: str) -> bool:
    hive_map = {"HKCU": winreg.HKEY_CURRENT_USER, "HKLM": winreg.HKEY_LOCAL_MACHINE}
    hive = hive_map.get(hive_str)
    if not hive:
        return False
    try:
        hk = winreg.OpenKey(hive, key_path, 0, winreg.KEY_SET_VALUE)
        winreg.DeleteValue(hk, name)
        winreg.CloseKey(hk)
        return True
    except Exception:
        return False

