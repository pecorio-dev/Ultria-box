def _clear_frame(frame):
    for w in frame.winfo_children():
        w.destroy()

def _make_row(parent, item: dict, idx: int, action_cb=None, action_label="Delete"):
    reasons = item.get("reasons", [])
    is_sus  = bool(reasons)
    bg = colors["dark_gray"] if idx % 2 == 0 else "#202020"

    row = ctk.CTkFrame(parent, fg_color=bg, corner_radius=4)
    row.pack(fill="x", padx=4, pady=2)

    badge_bg  = colors["dark_red"] if is_sus else "#1a4d2e"
    badge_txt = "⚠ THREAT" if is_sus else "✓ CLEAN"
    ctk.CTkLabel(row, text=badge_txt, fg_color=badge_bg, corner_radius=4,
                 font=ctk.CTkFont(size=10, weight="bold"),
                 width=80, height=22, text_color="white").pack(side="left", padx=(8, 6), pady=8)

    info = ctk.CTkFrame(row, fg_color="transparent")
    info.pack(side="left", fill="x", expand=True, pady=4)

    name  = item.get("name", "?")
    extra = ""
    if "pid"  in item: extra = f"   PID {item['pid']}"
    if "hive" in item: extra = f"   [{item['hive']}\\…\\Run]"

    ctk.CTkLabel(info, text=name[:52] + extra,
                 font=ctk.CTkFont(size=12, weight="bold"),
                 text_color="white", anchor="w").pack(fill="x")

    path = item.get("path", "")
    if path and path.lower() != name.lower():
        ctk.CTkLabel(info, text=path[:78],
                     font=ctk.CTkFont(size=10), text_color="#777777", anchor="w").pack(fill="x")

    if reasons:
        ctk.CTkLabel(info, text=" · ".join(reasons)[:95],
                     font=ctk.CTkFont(size=10), text_color="#ffcc44", anchor="w").pack(fill="x")

    if is_sus and action_cb:
        ctk.CTkButton(row, text=action_label, width=68, height=26,
                      command=lambda i=item: action_cb(i),
                      fg_color=colors["dark_red"], hover_color=colors["red"],
                      font=ctk.CTkFont(size=11)).pack(side="right", padx=8)

def _tab_skeleton(tab_frame):
    tab_frame.grid_rowconfigure(1, weight=1)
    tab_frame.grid_columnconfigure(0, weight=1)
    top    = ctk.CTkFrame(tab_frame, height=46, fg_color=colors["background"])
