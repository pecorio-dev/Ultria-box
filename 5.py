def scan_processes() -> list[dict]:
    results = []
    try:
        import psutil
        for proc in psutil.process_iter(['pid', 'name', 'exe']):
            try:
                name = proc.info['name'] or ""
                path = proc.info['exe'] or name
                pid  = str(proc.info['pid'])
                reasons = _score(name, path)
                results.append({"pid": pid, "name": name, "path": path, "reasons": reasons})
            except (psutil.NoSuchProcess, psutil.AccessDenied):
                pass
        return results
    except ImportError:
        pass

    try:
        raw = subprocess.check_output(
            ["tasklist", "/FO", "CSV", "/NH"],
            stderr=subprocess.DEVNULL, creationflags=subprocess.CREATE_NO_WINDOW,
            timeout=12
        ).decode("utf-8", "ignore")
        for line in raw.splitlines():
            line = line.strip()
            if not line:
                continue
            parts = [p.strip('"') for p in line.split('","')]
            if len(parts) < 2:
                continue
            name = parts[0]; pid = parts[1]
            if not name or name == "Image Name":
                continue
            reasons = _score(name, name)
            results.append({"pid": pid, "name": name, "path": name, "reasons": reasons})
        return results
    except Exception:
        pass

    try:
        raw = subprocess.check_output(
            ["wmic", "process", "get", "ExecutablePath,Name,ProcessId", "/format:csv"],
            stderr=subprocess.DEVNULL, creationflags=subprocess.CREATE_NO_WINDOW,
            timeout=15
        ).decode("utf-8-sig", "ignore")
        for line in raw.splitlines():
            line = line.strip()
            if not line or "," not in line:
                continue
            parts = [p.strip() for p in line.split(",")]
            if len(parts) < 4 or parts[2].lower() in ("name", ""):
                continue
            _, path, name, pid = parts[0], parts[1], parts[2], parts[3]
            if not name:
                continue
            reasons = _score(name, path or name)
            results.append({"pid": pid, "name": name, "path": path or name, "reasons": reasons})
    except Exception:
        pass

    return results

def kill_process(pid: str, name: str) -> bool:
    try:
        subprocess.run(
            ["taskkill", "/F", "/PID", pid],
            capture_output=True, creationflags=subprocess.CREATE_NO_WINDOW
        )
        return True
    except Exception:
        return False

