_SCAN_DIRS = [
    os.environ.get("TEMP", ""),
    os.environ.get("TMP", ""),
    os.path.join(os.environ.get("USERPROFILE", ""), "AppData", "Roaming"),
    os.path.join(os.environ.get("USERPROFILE", ""), "AppData", "Local", "Temp"),
    r"C:\Users\Public",
    r"C:\Windows\Temp",
]

def scan_files() -> list[dict]:
    results = []
    for directory in _SCAN_DIRS:
        if not directory or not os.path.isdir(directory):
            continue
        try:
            for fname in os.listdir(directory):
                if not fname.lower().endswith((".exe", ".py", ".pyw", ".bat", ".vbs", ".ps1")):
                    continue
                fpath = os.path.join(directory, fname)
                reasons = _score(fname, fpath)
                if fname.lower().endswith(".exe"):
                    sig = _scan_binary(fpath)
                    if sig and sig not in reasons:
                        reasons.append(sig)
                if not reasons:
                    reasons.append("executable in drop zone")
                results.append({"name": fname, "path": fpath, "reasons": reasons})
        except PermissionError:
            pass
    return results

def delete_file(path: str) -> bool:
    try:
        os.remove(path)
        return True
    except Exception:
        return False

